/** 关卡所属 zoneId 的快速查找表 */
const levelZoneMap: Record<string, string> = {};
for (const level of levels) {
  levelZoneMap[level.id] = level.zoneId;
}

/**
 * 根据用户已通关关卡和用户等级，派生技能树状态。
 * currentLevel 通过所属 zone 内已通关关卡数按顺序分配到各技能；
 * isUnlocked 取决于用户等级 ≥ requiredLevel 且前置技能已满级。
 */
export function deriveSkillTree(
  completedLevelIds: string[],
  userLevel: number,
): Array<SkillNodeDef & { currentLevel: number; isUnlocked: boolean }> {
  // 统计每个 zone 已通关关卡数
  const zoneCompletedCount: Record<string, number> = {};
  for (const levelId of completedLevelIds) {
    const zoneId = levelZoneMap[levelId];
    if (zoneId) {
      zoneCompletedCount[zoneId] = (zoneCompletedCount[zoneId] ?? 0) + 1;
    }
  }

  // 按 category 分组并保留顺序
  const categoryOrder = ["HTML基础", "CSS样式", "JavaScript", "前端框架", "后端开发", "数据库"];
  const grouped: Record<string, SkillNodeDef[]> = {};
  for (const cat of categoryOrder) grouped[cat] = [];
  for (const node of skillNodes) {
    if (grouped[node.category]) grouped[node.category].push(node);
  }

  const result: Array<SkillNodeDef & { currentLevel: number; isUnlocked: boolean }> = [];
  const unlockedNodes = new Set<string>(); // nodes with currentLevel >= 1

  // 先计算所有节点的 currentLevel（按 category 顺序分配）
  for (const cat of categoryOrder) {
    const nodes = grouped[cat] ?? [];
    if (nodes.length === 0) continue;

    // 获取该 category 下所有技能对应 zone 的已通关数
    const catZoneIds = [...new Set(nodes.filter((n) => n.zoneId).map((n) => n.zoneId!))];
    const totalCompleted = catZoneIds.reduce((sum, zid) => sum + (zoneCompletedCount[zid] ?? 0), 0);

    let remaining = totalCompleted;
    const tempLevels: number[] = [];
    for (const node of nodes) {
      const level = Math.min(node.maxLevel, remaining);
      tempLevels.push(level);
      remaining -= level;
    }

    for (let i = 0; i < nodes.length; i++) {
      result.push({ ...nodes[i], currentLevel: tempLevels[i], isUnlocked: false });
      if (tempLevels[i] >= 1) {
        unlockedNodes.add(nodes[i].id);
      }
    }
  }

  // 再计算 isUnlocked：等级达标 + 所有前置技能已至少解锁（≥1 级）
  for (const item of result) {
    const prereqsMet = item.requiredNodeIds.every((rid) => unlockedNodes.has(rid));
    item.isUnlocked = userLevel >= item.requiredLevel && prereqsMet;
  }

  return result;
}